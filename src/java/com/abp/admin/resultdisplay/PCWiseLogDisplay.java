public class PCWiseLogDisplay {
    
    private String pcid;
    private String theorymarks;
    private int scoredtheorymarks;
    private String practicalmarks;
    private int scoredpracticalmarks;
    private String totalmarks;
    private String scoredtotalmarks;

    public String getPcid() {
        return pcid;
    }

    public void setPcid(String pcid) {
        this.pcid = pcid;
    }

    public String getTheorymarks() {
        return theorymarks;
    }

    public void setTheorymarks(String theorymarks) {
        this.theorymarks = theorymarks;
    }

    public int getScoredtheorymarks() {
        return scoredtheorymarks;
    }

    public void setScoredtheorymarks(int scoredtheorymarks) {
        this.scoredtheorymarks = scoredtheorymarks;
    }

    public String getPracticalmarks() {
        return practicalmarks;
    }

    public void setPracticalmarks(String practicalmarks) {
        this.practicalmarks = practicalmarks;
    }

    public int getScoredpracticalmarks() {
        return scoredpracticalmarks;
    }

    public void setScoredpracticalmarks(int scoredpracticalmarks) {
        this.scoredpracticalmarks = scoredpracticalmarks;
    }

    public String getTotalmarks() {
        return totalmarks;
    }

    public void setTotalmarks(String totalmarks) {
        this.totalmarks = totalmarks;
    }

    public String getScoredtotalmarks() {
        return scoredtotalmarks;
    }

    public void setScoredtotalmarks(String scoredtotalmarks) {
        this.scoredtotalmarks = scoredtotalmarks;
    }
    
    
    
    
    
}
