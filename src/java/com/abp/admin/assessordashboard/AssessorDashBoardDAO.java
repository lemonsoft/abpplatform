package com.abp.admin.assessordashboard;

/**
 *
 * @author ss
 */
public class AssessorDashBoardDAO {
    
    private String srno;
    private String jobrole;
    private String totalassessor;
    private String state;
    
    private String sscid;
    private String location;

    public String getSrno() {
        return srno;
    }

    public void setSrno(String srno) {
        this.srno = srno;
    }

    public String getJobrole() {
        return jobrole;
    }

    public void setJobrole(String jobrole) {
        this.jobrole = jobrole;
    }

    public String getTotalassessor() {
        return totalassessor;
    }

    public void setTotalassessor(String totalassessor) {
        this.totalassessor = totalassessor;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getSscid() {
        return sscid;
    }

    public void setSscid(String sscid) {
        this.sscid = sscid;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }
    
    
    
    
}
