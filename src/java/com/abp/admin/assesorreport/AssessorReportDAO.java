public class AssessorReportDAO {
    
    private String srno;
    private String assessorname;
    private String jobroles;
    private String location;
    private String datesoccupiedinmonth;
    private String sscid;
    private String month;
    
    public String getSrno() {
        return srno;
    }

    public void setSrno(String srno) {
        this.srno = srno;
    }

    public String getAssessorname() {
        return assessorname;
    }

    public void setAssessorname(String assessorname) {
        this.assessorname = assessorname;
    }

    public String getJobroles() {
        return jobroles;
    }

    public void setJobroles(String jobroles) {
        this.jobroles = jobroles;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getDatesoccupiedinmonth() {
        return datesoccupiedinmonth;
    }

    public void setDatesoccupiedinmonth(String datesoccupiedinmonth) {
        this.datesoccupiedinmonth = datesoccupiedinmonth;
    }

    public String getSscid() {
        return sscid;
    }

    public void setSscid(String sscid) {
        this.sscid = sscid;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }
    
    
    
    
}
