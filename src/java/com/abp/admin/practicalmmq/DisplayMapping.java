public class DisplayMapping {
    
    private int id;
    private String checkbox;
    private String pcid;
    private String pcname;
    private String actualmarks;
    private String remainingmarks;
    private String marks;

    public String getCheckbox() {
        return checkbox;
    }

    public void setCheckbox(String checkbox) {
        this.checkbox = checkbox;
    }

    public String getPcid() {
        return pcid;
    }

    public void setPcid(String pcid) {
        this.pcid = pcid;
    }

    public String getPcname() {
        return pcname;
    }

    public void setPcname(String pcname) {
        this.pcname = pcname;
    }

    public String getActualmarks() {
        return actualmarks;
    }

    public void setActualmarks(String actualmarks) {
        this.actualmarks = actualmarks;
    }

    public String getRemainingmarks() {
        return remainingmarks;
    }

    public void setRemainingmarks(String remainingmarks) {
        this.remainingmarks = remainingmarks;
    }

    public String getMarks() {
        return marks;
    }

    public void setMarks(String marks) {
        this.marks = marks;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
    
    
}
