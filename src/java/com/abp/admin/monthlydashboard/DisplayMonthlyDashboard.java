public class DisplayMonthlyDashboard {
    
    private String srno;
    private String jobrole;
    private String pass;
    private String fail;
    private String notappeared;
    private String totalnostudent;
    private String sscid;
    private String month;
    

    public String getSrno() {
        return srno;
    }

    public void setSrno(String srno) {
        this.srno = srno;
    }

    public String getJobrole() {
        return jobrole;
    }

    public void setJobrole(String jobrole) {
        this.jobrole = jobrole;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getFail() {
        return fail;
    }

    public void setFail(String fail) {
        this.fail = fail;
    }

    public String getNotappeared() {
        return notappeared;
    }

    public void setNotappeared(String notappeared) {
        this.notappeared = notappeared;
    }

    public String getTotalnostudent() {
        return totalnostudent;
    }

    public void setTotalnostudent(String totalnostudent) {
        this.totalnostudent = totalnostudent;
    }

    public String getSscid() {
        return sscid;
    }

    public void setSscid(String sscid) {
        this.sscid = sscid;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }
    
    
    
    
}
